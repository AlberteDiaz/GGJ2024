using UnityEngine;
public class Audio : MonoBehaviour
{
    public static Audio instance;
    public static Audio Instance { get; private set; }

    void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(this);
        }
        else
        {
            Instance = this;
        }

        DontDestroyOnLoad(this.gameObject);
    }

    
}